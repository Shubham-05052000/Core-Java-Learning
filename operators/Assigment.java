package operators;

public class Assigment {
public static void main(String[] args) {
	int a = 10;
	System.out.println(a);//actual value
	a+=10;// Compound Assignment operators
	System.out.println(a);// value 20
	a-=10;
	System.out.println(a);//actual value
	//-----------------------------
	a*=10;
	System.out.println(a);//actual value
	a/=10;
	System.out.println(a);//actual value
	
}
}
